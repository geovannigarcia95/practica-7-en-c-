//7. Escriba un programa que calcule el valor de: 1+2+3+...+n.
#include <iostream>

using namespace std;

int main()
{
		int n, suma, i;
	suma=0;
	cout <<"numeros a sumar: ";
	cin >>n;
	for(i=1; i <= n ; i++)	
	{
		suma=suma+i;
	}
	cout <<"La suma de los numeros "<<n<< " numeros es: "<<suma<<endl;
}



